<p>The DOCNC was founded in 1939 by a foresightful group of alumni who wanted to recreate the Dartmouth experience out West. The club's stated purpose from our 1939 Articles of Incorporation is to encourage and promote "winter sports and all other sports, and social and recreational activities and friendly relations among the members." The DOCNC is the oldest alumni club on the West Coast and the only alumni club to own and manage a cabin -  just like the cabins in the Second College Grant.</p>

<img src="img/about/docncBruceLeeger1946.jpg" class="right border" />

<p>The cabin was built in the late 1940s using local materials, such as ties from the old railroad snow sheds that crossed Donner Summit. President Dickey visited the cabin in October, 1955 to dedicate the <a href="fireplace.php">cabin's remarkable fireplace to Ray Taylor, '11</a>, one of the cabin's founders and the man responsible for the fireplace's New Hampshire granite.</p>  

<p>The cabin is a truly magical place, a piece of New Hampshire transplanted to California. It has served as the entry point to Dartmouth on the West Coast for countless new students and graduates,  and it provides a way of reconnecting with the Dartmouth community for more "mature" alumni/ae.</p>

<p>As an alumni club, the DOCNC is loosely affiliated with the College, the Dartmouth Outing Club, and other Bay Area alumni clubs. Only graduates of Dartmouth College or one of its graduate programs can be DOCNC members, and only DOCNC members may reserve the cabin. Membership fees are $40/yearly for alumni who graduated in the last ten years, and $50 for those who graduated earlier. We have almost 400 members, ranging from current students to the class of '54.</p>

<p>DOCNC events are open to the entire Dartmouth community. We host an annual ski weekend at Sugarbowl Ski Resort – we call it Winter Carnival West – and a semi-annual Tubestock West in the summer. The Pacific Crest Trail runs through our "front yard."</p>

<p>Cabin improvements over the past few years include new mattresses, new industrial kitchen sink and counter, new couches, new carpet, new linoleum in the bathroom and toilet, refinished wood floor and a new dock. All improvements were done with volunteer work and from money from rental fees and membership dues.</p> 

