<p>We're working on a short history of the club's formation and the cabin's creation and hope to post this soon. In the meantime, here's a quick overview:</p> 

<blockquote style="margin: 20px; padding: 10px; border: solid #003300 1px;">
    <p>The DOCNC's history at Lake Mary began when Johnny Ellis '34 purchased a plot of land with the intent to build a small ski resort. Though those plans never fully materialized, Ellis did install a rope tow, one of the first in California. The remains of the rope tow structure and related buildings are still visible next to the cabin.</p> 

    <p>Eventually, the land passed to a group of area alumni &mdash; Johnny Ellis, '34, Wilson Evans, Raymond Taylor, '11, Richard Townsend, and B. D. Winslow &mdash; who incorporated the DOCNC on February 28th, 1939, the same year that the Sugarbowl Ski Resort opened. Outdoor and ski clubs were a very popular thing in the late 1920s and 1930s in Tahoe. The late 1930s ushered in several other large lodges on Donner Summit, including the <a href="http://www.sierraclub.org/outings/lodges/ctl/accomodations.asp">Sierra Club's Clair Tappaan Lodge</a> and the <a href="http://thenaturefriendscorporation.org/heidelmannnf.htm">Nature Friend's Heidelmann Lodge</a>. The DOCNC cabin was built with alumni labor from 1949-1955 from salvaged construction materials, such as a World War II Quonset Hut platform for the main floor and discarded railroad snowshed timbers for the walls.</p> 

    <p>In 1987, all 18 acres of Lake Mary and 82 acres with 21 building sites was sold by Southern Pacific to the Sugarbowl Ski Resort for about $300,000. Len Smith, Lou Leeger and Bill Perrell (and other Dartmouth alumni) worked with Lake Mary folks to buy the 11 acres around Lake Mary, including three parcels for the DOCNC. Lake Mary Partners was formed to do this and was later dissolved. These gentlemen put a significant amount of work into this, and we're eternally grateful!</p> 

    <p>The DOCNC removed brush and move rocks to create the cabin's beach in 1987. We bulldozed stumps, built a dock and put in sand over the following years.</p> 

    <p>In 1997, Bob Williams submitted excellent comments from the DOCNC on the 	proposed expansion of the Sugarbowl Ski Resort. Bob's response to Sugarbowl's Environmental Impact Statement is available at the cabin.</p>
</blockquote>

