<h3>Young Alumni Weekend</h3>

<p>Aug 26-28, 2011

<p>In conjunction with the Dartmouth Alumni of Silicon Valley and the Dartmouth Alumni of San Francisco, the DOCNC will host the second annual Young Alum Mountain Festival on August 26-28.  Young alums from across the Bay Area clubs convene at Lake Mary for a weekend of revelry in tribute to the late Dartmouth tradition, Tubestock.  With hiking, biking, and a Dartmouth-style feed Saturday night - you won't be disappointed.  RSVP REQUIRED: To aid in the planning of this event, please RSVP by August 5th to James von Rittmann '95 at president [AT] daasv.org. Please include the first and last name, and Dartmouth class (if applicable), of each attendee. RSVPs received after August 5th are welcome, but must pay a $10 Administration Fee. </p>

